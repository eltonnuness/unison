		aboutMenuItem
		        .setAccelerator(KeyStroke.getKeyStroke(KeyEvent.VK_HOME, InputEvent.CTRL_MASK));

		controller.refreshDataFromDatabase();
		splash.setProgress(100);

		splash.close();
